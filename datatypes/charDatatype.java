public class charDatatype {
    public static void main(String arg[])
    {
        char c1=65;
        char c2='A';
        System.out.println(c1);
        System.out.println(c2);
        for(int i=65;i<=128;i++)
        {
            char j=(char)i;
            System.out.println(j+" ");
        }
    }
    
}
