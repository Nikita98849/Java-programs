public class swap {
    public static void main(String arg[])
    {
        int a=10,b=20,temp;
        System.out.print("\nBefore swapping");
        System.out.print("\nA = "+a+" "+"B = "+b);
        System.out.print("\nAfter swapping");

        temp=a;
        a=b;
        b=temp;
        System.out.print("\nA = "+a+" "+"B = "+b);


    }
    
}
