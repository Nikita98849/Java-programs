public class dimand1
{
    public static void main(String arg[])
    {
        int i,j;
        for(i=0;i<=5;i++)
        {
            for(j=0;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.print("\n");
        }
        for(i=4;i>=0;i--)
        {
            for(j=0;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
    

