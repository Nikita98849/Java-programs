public class alphabeticpattern1 {
    public static void main(String arg[])
    {
        char i,j;
        for(i='A';i<='E';i++)
        {
            for(j='A';j<=i;j++)
            {
                System.out.print(j);
            }
            System.out.print("\n");
        }
    }
    
}
