public class variabledeclration {
    public static void main(String arg[])
    {
        int a=10;
        System.out.println("A = "+a);
        int _a=20;
        System.out.println("_A = "+_a);
        int $a=50;
        System.out.println("$A = "+$a);
        int a1=90;
        System.out.println("A1 = "+a1);
        int b=30;
        int c=b;
        System.out.println("C = "+c);
        int su_m=a+b;
        System.out.println("Sum = "+su_m);

        int a2=90;
        //System.out.println("A2 = "+A2);      A2 cannot be resolved to a variable

        int int=30;
        //System.out.print("Int = "+int);       Syntax error on token "int", invalid VariableDeclaratorI
         
    }
    
}
