public class onevalueMultiVar {
    public static void main(String arg[])
    {
        int a,b,c; 
        a=b=c=10;
        System.out.println("One value to multiple variable a = "+a);   //10  
        System.out.println("One value to multiple variable b = "+b);   //10
        System.out.println("One value to multiple variable c = "+c);     //10
        System.out.println("Addition of three numbers = "+(a+b+c));

    } 
    
}
